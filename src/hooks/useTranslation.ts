export function useTranslation() {
  const [lang, setLang] = useState("en");

  useEffect(() => {
    // Read initial session storage value
    if (typeof window !== "undefined") {
      const cached = sessionStorage.getItem("agrinex-lang");
      if (cached && DICTIONARY[cached]) {
        setLang(cached);
      }
    }

    const handleLangChange = (e: Event) => {
      const customEvent = e as CustomEvent;
      if (customEvent.detail?.code && DICTIONARY[customEvent.detail.code]) {
        setLang(customEvent.detail.code);
      }
    };

    if (typeof window !== "undefined") {
      window.addEventListener(LANG_CHANGE_EVENT, handleLangChange);
    }
    return () => {
      if (typeof window !== "undefined") {
        window.removeEventListener(LANG_CHANGE_EVENT, handleLangChange);
      }
    };
  }, []);

  const t = (key: string): string => {
    return DICTIONARY[lang]?.[key] ?? DICTIONARY["en"]?.[key] ?? key;
  };

  return { t, currentLanguage: lang };
}
